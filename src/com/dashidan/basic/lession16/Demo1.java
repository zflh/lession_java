public class Demo1 {
    public static void main(String[] args) {
        int a = 0;
        int b = 1;
        if (a > b) {
            System.out.println("a > b");
        } else if (a == b) {
            System.out.println("a == b");
        } else {
            System.out.println("a < b");
        }
    }
}
