package com.dashidan.basic.lession8;

/**
 * 大屎蛋教程网-dashidan.com
 *
 * Java教程基础篇: 8.Java复合运算符： +=, -=等.
 */
public class Demo1 {
    public static void main(String[] args) {
        int a = 0;
        /** += 运算*/
        a += 1;
        System.out.println(a);
        /** -= 运算*/
        int b = 0;
        b -= 1;
        System.out.println(b);
        /** *= 运算*/
        int c = 2;
        c *= 3;
        System.out.println(c);
        /** /= 运算*/
        int d = 5;
        d /= 3;
        System.out.println(d);
        /** %= 运算*/
        int e = 5;
        e %= 3;
        System.out.println(e);
    }
}
